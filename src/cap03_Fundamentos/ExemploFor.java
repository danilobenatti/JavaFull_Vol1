public class ExemploFor {
	
	public static void main(String[] args) {
		
		int i;
		
		for (i = 0; i < 5; i++) {
			System.out.println(i);
		}
		
		System.out.println("--- # ---");
		
		for (int j = 1; j <= 5; j++) {
			System.out.println(j);
		}
	}
}